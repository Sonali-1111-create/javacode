package assignpackage;

public class A32_Switchcase {
	public static void main(String[] args)
	{
		switch(0)
		{
		case 1:
			System.out.println("Mozilla browser launch");
			break;
		
		case 2:
			System.out.println("chrome browser launch");
			break;
		case 3:
			System.out.println("Firefox browser launch");
			break;
		case 4:
			System.out.println("Edge browser launch");
			break;
		default:
			System.out.println("Your selection is wrong,kindly press 1,2,3");
			
		
	}
	}
}
	


