package assignpackage;
import java.util.Scanner;

public class A47_switch_scanner {
	public static void main(String[] args)
	{
		Scanner s1=new Scanner(System.in);
		int input=s1.nextInt();
		switch(input)
		{
		case 1:
			System.out.println("Press 1 for launching chrome browser");
			break;
		case 2:
			System.out.println("Press 2 for launching mozilla browser");
			break;
		case 3:
			System.out.println("Press 3 for launching edge browser");
			break;
		case 4:
			System.out.println("Press 4 for launching firefox browser");
			break;
			
			
		}
		}
		

}
